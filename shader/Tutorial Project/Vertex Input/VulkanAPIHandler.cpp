void VulkanAPIHandler::onWindowResized(GLFWwindow * window, int width, int height) {
	if (width == 0 || height == 0) {
		return;
	}

	VulkanAPIHandler* app = reinterpret_cast<VulkanAPIHandler*>(glfwGetWindowUserPointer(window));
	app->recreateSwapChain();
}

